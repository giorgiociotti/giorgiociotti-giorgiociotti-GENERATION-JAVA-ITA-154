package com.generation.lessons;

public class EsercizioArray {
    public static void main(String[] args) {
        int[] numeri = {12, 45, 7, 23, 89};
        for (int i = 0; i < numeri.length; i++) {
            System.out.println("Elemento in posizione " + i + ": " + numeri[i]);
        }
    }
}